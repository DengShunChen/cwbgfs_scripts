#!/bin/bash

  #-- outfile
  outfile='ocards_WRF'
#  outfile='ocards'

#------------------------------------------------------------------------------------------------------------------#
#-- model levels
#------------------------------------------------------------------------------------------------------------------#
  #-- variables
  fs='SA1100 SA2100 SA3100 SA4100 SA15B0 SA25B0 SA35B0 SA45B0 SSL010 B00010 B00200 B00100 B00210 B00510 B00650 S00100 W00100 B02100 B10200 B10210 B10510 S005C0 S015B0 S01100 S02100'

  #-- taus 
  ts='000 003 004 005 006 007 008 009 012 018 024 030 036 042 048 054 060 066 072 078 084 090 096 102 108 114 120 126 132 138 144 150 156 162 168'

#------------------------------------------------------------------------------------------------------------------#
#-- pressure levels 
#------------------------------------------------------------------------------------------------------------------#

  lPresLevels="true"

  #-- variables of p levels
  pfs='vor h2o phi tmp wnd'
 
  #-- p levels (27 levels)
  pls='10 20 30 50 70 100 150 200 250 300 350 400 450 500 550 600 650 700 750 800 850 900 925 950 975 1000'

  #-- taus 
  pts='000 006 012 018 024 030 036 042 048 054 060 066 072 078 084 090 096 102 108 114 120 126 132 138 144 150 156 162 168'

#------------------------------------------------------------------------------------------------------------------#
# Below are working codes, no need to change if necessary.
#------------------------------------------------------------------------------------------------------------------#

rm -f $outfile
echo "Creating $outfile ..."

#-- model levels
  for f in $fs ; do 
    for t in $ts ; do

    line=`printf "%-6.6s      %3.3s" ${f} ${t}`
    echo "$line" >> $outfile

    done
  done

#-- pressure levels 
  if [ "$lPresLevels" == "true" ] ; then
  for f in $pfs ; do
    for l in $pls ; do
      for t in $pts ; do

      line=`printf "%-6.6s %4.4s %3.3s" ${f} ${l} ${t}`
      echo "$line" >> $outfile

      done
    done
  done
  fi

echo "nomodata" >> $outfile
